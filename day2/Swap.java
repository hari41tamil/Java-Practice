package day2;

public class Swap {
    public static void main(String[] args) {
        int a=4;
        int b=6;
        int c = a;
        System.out.println("The value of a is :"+a);
        System.out.println("The value of b is :"+b);
        a = b;
        b = c;
        System.out.println("The swapped value of a is :" +a + " value of b is :" +b);

    }    
}
